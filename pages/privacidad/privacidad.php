	<div class="tituloo">Declaración de privacidad</div>

	<p>En esta Declaración de privacidad se explican nuestras prácticas respecto de la recolección, uso, transferencia 
	y transmisión de cierta información, incluida su información personal, por parte de la nuestro grupo <strong>"W00tflix"</strong></p>

	<h3>Contacto</h3>

	<p>Si tiene alguna pregunta respecto de su cuenta, visite nuestro centro de ayuda online 
	<a href="#">https://ayuda.wootflix.com</a>, donde también encontrará información 
	sobre cómo contactarse con nuestro Servicio al Cliente para obtener asistencia. Si tiene alguna pregunta 
	respecto de esta Declaración de privacidad, o nuestro uso de su información personal, de las cookies y de 
	otras tecnologías similares, envíenos un email a w00tflix@hotmail.com</p>

	<p>Tenga en cuenta que si se contacta con nosotros para obtener ayuda, tanto por su seguridad como por la nuestra, 
	podemos pedirle que autentique su identidad antes de hacer la solicitud.</p>

	<h3>Recolección de la información</h3>

	<p>Recibimos y almacenamos información sobre usted, incluida:</p>

	<ul>
		<li><strong>La información que usted nos proporciona:</strong> Recopilamos la información que usted nos brinda, que incluye:

		<ul>
			<li>Su nombre, email, dirección, código postal, forma de pago y número de teléfono. Esta información se recopila de diferentes maneras, entre ellas, cuando usted la introduce en nuestro servicio, al interactuar con nuestro Servicio al Cliente o al participar en encuestas o promociones de marketing; y</li>
			<li>La información recopilada cuando usted elije proporcionar reseñas o calificaciones, cuando configura las preferencias en "Tu cuenta", sus preferencias generales, o nos brinda información a través de nuestro servicio o de otros medios.</li>
		</ul>
		</li>
		<li><strong>La información que recopilamos automáticamente:</strong> Recopilamos información con relación a usted, el uso de nuestro servicio y sus interacciones con nosotros y nuestra publicidad, además de aquella información relacionada con su computadora o cualquier dispositivo que utilice para acceder a nuestro servicio (como los sistemas de videojuegos, Smart TV, dispositivos móviles y decodificadores). Esta información incluye:
		<ul>
			<li>Su actividad en el servicio de W00flix, como las selecciones de los títulos, el historial de visualización y las consultas de búsquedas;</li>
			<li>Detalles sobre sus interacciones con el Servicio al Cliente, como la fecha, hora, razón de la consulta, transcripciones de cualquier conversación en el chat, y si nos llama, su número de teléfono;</li>
			<li>ID de dispositivos o identificadores únicos, características de software y dispositivo (como tipo y configuración), información de conexión, estadísticas sobre visualización de las páginas, URL de referencia, direcciones de IP e información de registro web estándar;</li>
			<li>La información recopilada a través del uso de cookies, señalizaciones web y otras tecnologías, incluidos los datos de publicidad (por ejemplo, información sobre las impresiones entregadas a una cookie, el URL del sitio donde se haya entregado la impresión, así como la fecha y la hora). Consulte nuestra sección de cookies y publicidad de Internet para obtener más información.</li>
		</ul>
		</li>
		<li><strong>La información de otras fuentes:</strong> Podemos complementar la información descrita anteriormente con información obtenida de otras fuentes, incluida la de proveedores de datos obtenidos tanto online como sin conexión a Internet. Esta información complementaria puede incluir datos demográficos, datos basados en los intereses y conductas de navegación por Internet.</li>
	</ul>

	<h3>Uso de la información</h3>

	<p>Usamos la información recopilada para proporcionar, analizar, administrar, mejorar y personalizar nuestros servicios e 
	iniciativas de marketing, los procesos de registro, pedidos y pagos; y para comunicarnos con usted sobre este y otros temas. 
	Por ejemplo, utilizamos la información que recopilamos para:</p>

	<ul>
		<li>Determinar la zona geográfica general donde se encuentra, ofrecerle contenidos localizados, recomendarle de manera personaliza películas 
		y programas de TV que consideramos que le pueden agradar, determinar su proveedor de servicio de Internet y para ayudarnos a responder con 
		mayor rapidez y eficiencia a las consultas y solicitudes;</li>
		<li>Prevenir, detectar e investigar actividades potencialmente prohibidas o ilegales, como el fraude, y hacer cumplir nuestros términos de 
		uso (por ejemplo, determinar si se le ofrece un período de prueba gratis);</li>
		<li>Analizar y comprender a nuestro público; mejorar nuestro servicio (incluida la experiencia con la interfaz de usuario), la optimización 
		de entrega, la selección de contenido, y los algoritmos de recomendación;</li>
		<li>Comunicarnos con usted en relación con nuestro servicio (por ejemplo, por email, notificaciones y mensajes de texto), para poder enviarle
		noticias sobre W00flix, detalles sobre nuevas funciones y contenidos disponibles en W00flix, así como ofertas especiales, anuncios 
		promocionales y encuestas para consumidores, y para ayudarlo con solicitudes operativas, como restablecer su contraseña. Consulte la 
		sección "Sus opciones" en esta Declaración de privacidad para saber cómo configurar o cambiar sus preferencias de comunicación.</li>
	</ul>

	<h3>Transferencia de la información</h3>

	<p>Transferimos su información para determinados propósitos y a terceros, de la siguiente manera:</p>

	<ul>
		<li><strong>La familia de empresas de W00flix:</strong> Compartimos su información con la familia de empresas de W00flix 
		(<a href="#">https://ayuda.wootflix.com/</a>) según sea necesario para: procesar y 
		almacenar datos; proveer acceso a nuestros servicios; proveer servicio al cliente; tomar decisiones sobre mejoras en el servicio y
		desarrollo de contenido; y de acuerdo con lo descrito en la sección de Uso de la información en esta Declaración de privacidad.</li>
		<li><strong>Proveedores de servicios:</strong> Usamos otras empresas, agentes o contratistas (“Proveedores de servicios”) para que presten
		servicios en nuestro nombre o para que nos ayuden en la prestación del servicio. Por ejemplo, contratamos proveedores de servicios para 
		ofrecer servicios de marketing, comunicaciones, infraestructura y servicios de TI, que personalizan y mejoran nuestro servicio, procesan 
		las transacciones de las tarjetas de crédito y de otras formas de pago, brindan atención al cliente, cobran deudas, analizan y mejoran los
		datos, incluida la información relativa a la interacción de nuestros usuarios con nuestro sitio web, y procesan nuestras encuestas de consumo.
		En el transcurso de la prestación de dichos servicios, estas empresas pueden tener acceso a su información personal o de otro tipo. No las
		autorizamos para que usen o transfieran su información personal, al menos que esté relacionada con los servicios que proporcionan.</li>
		<li><strong>Promociones:</strong> Podemos ofrecer promociones o programas conjuntos que requieran el intercambio de información con terceros
		como condición para participar. Al realizar este tipo de promociones, podremos compartir su nombre y otra información relacionada con la 
		realización del incentivo. Tenga en cuenta que los terceros son responsables de sus propias prácticas de privacidad.</li>
		<li><strong>Protección de W00flix y otros:</strong> W00flix y sus Proveedores de servicios pueden transferir información personal cuando 
		nosotros consideremos o ellos consideren razonable que dicha transferencia sea necesaria para (a) cumplir con cualquier legislación,
		reglamentación, proceso legal o solicitud gubernamental correspondiente; (b) hacer cumplir términos de uso correspondientes, incluida la 
		investigación de posibles infracciones; (c) detectar, prevenir o abordar actividades ilegales o presuntamente ilegales (incluidas las de 
		pago fraudulento), problemas de seguridad o técnicos; o (d) proteger de daño contra los derechos, la propiedad o la seguridad de W00flix, 
		sus usuarios o el público, según lo requiera o permita la ley.</li>
		<li><strong>Transferencias de negocios:</strong> En relación con una reorganización, reestructuración, fusión o venta, o cualquier otra 
		transferencia de bienes, nosotros transferiremos información, incluida la personal, a condición de que el destinatario se comprometa a
		respetar su información personal de conformidad con nuestra Declaración de privacidad.</li>
	</ul>

	<p>Cuando compartamos información personal con países fuera del Espacio Económico Europeo y otras regiones que tienen leyes de protección de datos exhaustivas, nos aseguraremos de que dicha información se transfiera de acuerdo con esta Declaración de privacidad y con las leyes correspondientes sobre protección de datos.</p>

	<p>Usted también puede elegir la transferencia de su información de las siguientes maneras:</p>

	<ul>
		<li>Al usar el servicio de W00flix, tendrá la oportunidad de publicar reseñas u otra información pública, y terceros pueden usar esa información;</li>
		<li>Ciertas áreas de nuestro servicio pueden incluir una herramienta que le permite compartir información por email, mensaje de texto y aplicaciones sociales o de otro tipo mediante los clientes y aplicaciones en su dispositivo inteligente;</li>
		<li>Los plugins o complementos sociales (incluidos los que ofrece Facebook, Twitter, Pinterest y Google) le permiten compartir información a través de esas plataformas.</li>
	</ul>

	<p>Las mismas redes sociales manejan estos plugins y estas aplicaciones, y están sujetos a sus términos de uso y políticas de privacidad.</p>

	<h3>Acceso a "Tu cuenta" y "Perfiles"</h3>

	<p>A efectos de facilitarle el acceso a su cuenta y para administrar mejor el servicio de W00flix, implementamos una tecnología que nos permite 
	reconocerlo como el dueño de la cuenta y darle acceso directo a esta sin solicitarle que vuelva a introducir ninguna contraseña u otra
	identificación de usuario al volver a ingresar al servicio de W00flix. Usted puede optar por no aprovechar esta función en su dispositivo 
	desmarcando la casilla "Recordarme en esta computadora" cuando inicia sesión en el sitio web. Para eliminar el acceso a su cuenta de W00flix 
	desde sus dispositivos: (a) vaya a "Tu cuenta", elija "Cerrar sesión en todos los dispositivos" y siga las instrucciones para desactivar sus 
	dispositivos (tenga en cuenta que la desactivación puede no ser inmediata) o (b) elimine la configuración de W00flix de su dispositivo (los pasos 
	necesarios varían según el dispositivo, y la opción no está disponible en todos los dispositivos). Siempre que sea posible, los usuarios de
	dispositivos públicos o compartidos deben cerrar sesión al finalizar cada visita. Si vende o devuelve una computadora o un dispositivo listo
	para W00flix, debe cerrar sesión y desactivar el dispositivo antes de hacerlo. Si usted no mantiene de forma segura su contraseña o su 
	dispositivo, o no cierra sesión o desactiva su dispositivo, los usuarios posteriores podrán acceder a la información de su cuenta, incluida 
	su información personal.</p>

	<p>Si comparte o permite que otros tengan acceso a su cuenta, podrán ver su información, incluida, en algunos casos, la información personal, 
	como su historial de visualización, calificaciones, reseñas e información de la cuenta (incluida su dirección de email u otra información en 
	"Tu cuenta"). Esto es así incluso si usa nuestra función de perfiles.</p>

	<p>Los perfiles permiten que los distintos miembros de su hogar tengan su propia experiencia personalizada de W00flix, de acuerdo
	con las series y las películas que cada uno disfruta, como recomendaciones personalizadas e historiales de visualización separados.
	Tome en consideración que los perfiles estarán libremente disponibles para cualquiera que use su cuenta de W00flix, toda persona que
	tenga acceso a su cuenta de W00flix puede navegar, usar, editar o borrar cualquier perfil. Esto debe ser explicado a las otras personas 
	que tengan acceso a su cuenta, si no desea que usen o actualicen su perfil, asegúrese de que estén al tanto de dicha situación.</p>

	<h3>Sus opciones</h3>

	<p>Si ya no desea que nos comuniquemos con usted mediante email o mensaje de texto, simplemente acceda a la página de configuración del 
	email o los mensajes de texto en la sección "Tu cuenta" en nuestro sitio web y desmárquelos para cancelar la suscripción. Alternativamente, 
	haga clic en el enlace "cancelar suscripción" en el email o responda STOP (o como se le indique) al mensaje SMS. Tome en consideración que 
	no podrá cancelar la suscripción a cierta correspondencia de email de nosotros, como los mensajes relacionados con las transacciones de su 
	cuenta.</p>

	<p>Puede dejar de recibir las notificaciones automáticas móviles de W00flix. Si posteriormente decide que ya no desea recibir estas 
	notificaciones, puede utilizar la configuración de su dispositivo móvil para desactivarlas.</p>
	
	
