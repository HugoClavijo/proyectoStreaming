	<h3 id="cookies">Cookies y publicidad de Internet</h3>

	<p>Nosotros y nuestros Proveedores de servicio utilizamos cookies y otras tecnologías (como señalizaciones web) por diferentes razones. Por ejemplo, utilizamos cookies y otras tecnologías para facilitar el acceso a nuestros servicios y recordarlo cuando vuelva a ingresar, proveer y analizar nuestros servicios, aprender más sobre nuestros usuarios y sus probables intereses, y presentar y personalizar el marketing o la publicidad. Queremos que esté informado sobre el uso que hacemos de estas tecnologías, de modo que este aviso explica los tipos de tecnología que usamos, cómo se comportan y cuáles son sus opciones de uso.</p>

	<ol>
		<li><strong>¿Qué son las cookies?</strong>

		<p>Las cookies son archivos de datos pequeños que usualmente se almacenan en su dispositivo cuando navega y utiliza sitios web y otros servicios online. Se emplean extensamente para que funcionen los sitios web, o para que sean más eficientes, así como para proveer información de reporte y ayudar con el servicio o la personalización de la publicidad.</p>

		<p>Las cookies no son el único tipo de tecnología que permite el funcionamiento; también empleamos otras tecnologías similares. A continuación encontrará más información y ejemplos.</p>
		</li>
		<li><strong>¿Por qué W00flix utiliza cookies?</strong>
		<p>Nosotros y nuestros Proveedores de servicio podemos usar los siguientes tipos de cookies:</p>

		<ul>
			<li><strong>Cookies esenciales:</strong> Estas cookies son estrictamente necesarias para nuestro sitio web o servicio online. 
			Por ejemplo, nosotros y nuestros Proveedores de servicio podemos usar estas cookies para autenticar e identificar a nuestros 
			miembros cuando utilizan nuestros sitios web y aplicaciones, de modo que podamos proveerles nuestros servicios. Además, nos ayudan 
			a aplicar nuestros Términos de uso, evitar fraudes y mantener la seguridad del servicio.</li>
			<li><strong>Cookies de rendimiento y funcionamiento:</strong> Estas cookies no son esenciales, pero nos ayudan a personalizar y 
			mejorar su experiencia online con W00flix. Por ejemplo, nos ayudan a recordar sus preferencias para que no tenga que volver a
			escribir la información ya provista (por ejemplo, durante el inicio de sesión). También utilizamos estas cookies para recolectar 
			información (por ejemplo, páginas populares, tarifas de cambio, patrones de vista, proporción de clics y otra información) sobre 
			el uso que hacen los visitantes del servicio de W00flix, de modo que podamos mejorarlo y personalizarlo, así como nuestro sitio web,
			y realizar investigación de mercado. Si se borran estas cookies, el funcionamiento de nuestro servicio se verá limitado.</li>
			<li><strong>Cookies de publicidad:</strong> Estas cookies utilizan información sobre su visita a este y otros sitios web (las 
			páginas que visita), el uso que hace del servicio o su respuesta a los anuncios comerciales y los emails, para presentar anuncios 
			más relevantes para usted. Este tipo de anuncios comerciales se llama "publicidad basada en el interés del usuario". Muchas de las 
			cookies de anunciante asociadas a nuestro servicio pertenecen a nuestros Proveedores de servicio.</li>
		</ul>
		</li>
		<li><strong>¿De qué modo puedo elegir qué cookies y otros tipos de rastreo online quiero aceptar?</strong>
			<p>Para obtener más información sobre las cookies de nuestro sitio web, así como sobre otros tipos
			de rastreo online (incluida la recolección de información hecha por terceros sobre sus actividades 
			online a lo largo del tiempo y en todos los sitios web o servicios online de terceros para publicidad
			basada en el interés del usuario), y para tomar las decisiones correspondientes 
			<!--a href="https://c.betrad.com/netflix/customlink.html?locale=es-EC" target="_blank">haga clic aquí</a-->.
			Por el momento, no respondemos a las señales de "no rastrear" del navegador web.</p>
		</li>
		<li><strong>¿De qué manera W00tflix utiliza las señalizaciones web y otras tecnologías?</strong>
		<p>Las señalizaciones web (también conocidas como "clear.gif" o "etiqueta de pixel") a menudo funcionan junto con las cookies. Nosotros y nuestros Proveedores de servicio podemos usarlas con propósitos similares a los de las cookies, como comprender y mejorar el uso de nuestro servicio, mejorar el rendimiento del sitio, controlar el tráfico de visitantes y las acciones sobre nuestro sitio, y comprender las interacciones con nuestro marketing (incluidos los emails y los anuncios comerciales online en sitios de terceros). Puesto que a menudo funcionan en conjunto, en muchos casos rechazar las cookies reducirá la eficacia de las señalizaciones web.</p>

		<p>Utilizamos otras tecnologías similares a las cookies, como almacenamiento en el navegador y plugins (p. ej., HTML5, IndexedDB, y WebSQL). Al igual que las cookies, algunas de estas tecnologías pueden almacenar pequeñas cantidades de datos en su dispositivo. Podemos utilizar estas y otras tecnologías con propósitos similares a los de las cookies, como para aplicar nuestros términos, evitar fraudes y analizar el uso de nuestro servicio. Existen varias maneras de elegir cómo se utilizan estas tecnologías. Por ejemplo, muchos navegadores populares permiten eliminar los datos almacenados, usualmente en el área de configuración; consulte la ayuda o el soporte de su navegador para obtener más información. En otras tecnologías, como el almacenamiento en Silverlight, los datos pueden eliminarse desde la aplicación.</p>
		</li>
	</ol>

