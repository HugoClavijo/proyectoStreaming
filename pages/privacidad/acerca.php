	<div class="tituloo">Acerca de W00tflix</div>

	<p>Fundada en 22 de Octubre, Wootflix es la principal plataforma de  entretenimiento e interacción social de la comunidad de Artistas y/o 
	Diseñadores, cultura del arte urbano y artes creativas del mundo más selecta. Cada día, cerca de 10 millones de visitantes se reúnen para ver 
	y admirar el arte en vivo con más de 2 millones de emisores. Te invitamos a unirte a los millones de personas que vienen a W00tflix a transmitir, 
	ver e interactuar entre ellos acerca de estas pasiones que les unen.</p>

	
	
	
	<h3>¿Por qué emite la gente?</h3>
	<p>Además de ser divertido, es una nueva y emocionante red social mediante la que conectar con amigos y fans con los que se comparte el amor por 
	el arte y los proyectos creativos. Muchos emisores se ganan la vida únicamente con W00tflix, entreteniendo a su público e interactuando con él.</p>

	
	
	<h3>¿Por qué la gente quiere ver W00tflix?</h3>
	<ul>
	  <li>A la gente le gusta ver cómo expresan su parte artística otras personas con grandes habilidades y se entretiene muchísimo cuando hay 
	  intereses compartidos. </li>
	  <li>W00tflix es un medio de descubrimiento con <a href="#">probada</a> influencia en decisiones de compra de videojuegos.</li>
	  <li>W00tflix es algo más que ser un mero espectador. Es vídeo social que dispone de audio y chat para que los emisores y sus audiencias 
	  puedan interactuar en tiempo real sobre temas artisticos.</li>
	</ul>

	
	
	<h3>¿Qué ve la gente?</h3>
	<ul>
		<li><strong>Teatro</strong>: Forma parte del publico en el teatro callejero y privado transmitido en vivo.</li>
		<li><strong>Programación generada por el usuario</strong>: contenido original creado por miembros de la comunidad, tanto si 
		se trata de arte improvisado con amigos, un monólogo o un diseño de algun grupo artístico.</li>
		<li><strong>Pintura</strong>: Las expresiones artisticas causan conmoción en la gente por lo que  damos la oportunidad de que el 
		artista pueda mostrar su arte en vivo.</li>
		<li><strong>Danza</strong>: La gente que realiza sus  actividades de danza en los exteriores para llamar gente usa esta plataforma.</li>
		<li><strong>Publicación</strong>: todos los principales sitios de medios de arte y diseño tienen un canal de W00tflix para mostrar
		contenidos en directo a su audiencia.</li>
		<li><strong>Eventos</strong>: por todo el mundo se celebran convenciones en las que se incorpora W00flix en los paneles, las 
		conferencias de prensa y los espectáculos.</li>
		
	</ul>
	
	
